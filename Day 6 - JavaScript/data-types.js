// Data Types / Primitives data types

// Numbers: Represents numeric CSSFontFeatureValuesRule. e.g. 5, 3, 1.5
let age = 25;
console.log("The value of age is:", age);
console.log("The type of age is", typeof (age));

// Strings: textual data enclosed in '' or "", e.g abcd1234!#$%
let name = "King";
console.log("The value of name is:",(name));
console.log("The type of name is:", typeof(name))

// Boolean: Either true of fales
let isAdmin = false;
console.log("The value of isAdmin is:", isAdmin);
console.log("The type of isAdmin is:", typeof(isAdmin));

// Null: Intentional absence of value
let car = null;
console.log("The value of car is: ", car);
console.log("The type of car is: ", typeof(car));

// Undefined: Unintentional absence of value
let city;
console.log("The type of city: ", city);
console.log("The type of city: ", typeof(city));