// Arithmetic Operations
let x = 10;
let y = 3;

// Sum 
let sum = x + y;
console.log("The sum is: ", sum);

// Difference
let difference = x - y;
console.log("The difference is: ", difference);

// Product
let product = x * y;
console.log("The product is: ", product);

// Quotient
let quotient = x / y;
console.log("The product is: ", quotient);

// Remainder or Modulo
let modulo = x % y;
console.log("The remainder is: ", modulo);

let answer = 10 + 2 * 23 / 2;
console.log("The answer is:", answer)

// Concatenation
let a = "Hello";
let b = "World!";

console.log(a + " " + b);

// Assignment Expressions
let i = 40;
let j = 20;

// +=
i += j;
console.log(i)

// -=
i -= j;
console.log(i)

// *=
i *= j;
console.log(i)

// /=
i /= j;
console.log(i)
